	//-----Private Data-----

	ID3D11Device* m_device = nullptr;
	ID3D11DeviceContext* m_context = nullptr;
	IDXGISwapChain* m_swapChain = nullptr;

	ColorTargetView* m_frameBackbufferRtv = nullptr;
