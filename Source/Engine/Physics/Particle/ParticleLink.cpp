//-------------------------------------------------------------------------------------------------
ParticleLink::ParticleLink(Particle* a, Particle* b)
{
	m_particles[0] = a;
	m_particles[1] = b;
}


//-------------------------------------------------------------------------------------------------
float ParticleLink::GetCurrentLength() const
{
	return (m_particles[1]->GetPosition() - m_particles[0]->GetPosition()).GetLength();
}
