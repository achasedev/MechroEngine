Entity::Entity()
	: m_id(s_nextEntityID)
{
	++s_nextEntityID;

	collisionPrimitive = new BoxCollider(this, OBB3(Vector3::ZERO, Vector3(0.5f), Quaternion::IDENTITY));
}


