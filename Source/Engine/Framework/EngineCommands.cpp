//-------------------------------------------------------------------------------------------------
void Command_Add(CommandArgs& args)
{
	float a = args.GetNextFloat();
	float b = args.GetNextFloat();

	ConsolePrintf("%.2f + %.2f = %.2f", a, b, a + b);
}
