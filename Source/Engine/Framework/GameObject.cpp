GameObject::~GameObject()
{
	if (m_rigidBody != nullptr)
	{
		PhysicsScene2D* physicsScene = m_rigidBody->GetScene();
		physicsScene->RemoveGameObject(this);
	}

	SAFE_DELETE_POINTER(m_shape);
}
