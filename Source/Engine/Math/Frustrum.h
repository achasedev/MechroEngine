class Frustrum
{
public:
	//-----Public Methods-----

	Frustrum();
	Frustrum(const Matrix4& view, const Matrix4& projection);

	void	Set(const Matrix4& view, const Matrix4& projection);

	bool	IsPointInside(const Vector3& point) const;
	void	GetPlanes(std::vector<Plane3> out_planes) const;
	Vector3 GetPoint(int pointIndex) const { return m_points[pointIndex]; }


private:
	//-----Private Data-----

	Vector3 m_points[8]; // Ordered bottom-left-back, top-left-back, top-right-back, bottom-right-back, front-bottom-right, front-top-right, front-top-left, front-bottom-left (remember front is +z, back is -z)
	Plane3 m_planes[6];

};

