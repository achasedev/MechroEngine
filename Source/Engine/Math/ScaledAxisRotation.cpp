//-------------------------------------------------------------------------------------------------
ScaledAxisRotation::ScaledAxisRotation(const Vector3& scaledAxis)
	: data(scaledAxis)
{
}


//-------------------------------------------------------------------------------------------------
ScaledAxisRotation::ScaledAxisRotation(const Vector3& axis, float angle)
{
	data = axis.GetNormalized() * angle;
}


//-------------------------------------------------------------------------------------------------
Vector3 ScaledAxisRotation::GetAxisNormalized() const
{
	return data.GetNormalized();
}


//-------------------------------------------------------------------------------------------------
float ScaledAxisRotation::GetAngle() const
{
	return data.GetLength();
}
