Point::Point(const Vector3& position)
{
	x = position.x;
	y = position.y;
	z = position.z;
}


//-------------------------------------------------------------------------------------------------
int Point::GetSupportPoint(const Vector3& direction, Vector3& out_supportPt) const
{
	UNUSED(direction);
	out_supportPt = Vector3(x, y, z);
	return -1;
}
