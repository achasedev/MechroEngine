//-------------------------------------------------------------------------------------------------
Capsule3::Capsule3(const Vector3& _start, const Vector3& _end, float _radius)
	: start(_start), end(_end), radius(_radius)
{
}


//-------------------------------------------------------------------------------------------------
float Capsule3::GetHeight() const
{
	Vector3 spine = end - start;
	return spine.GetLength();
}


//-------------------------------------------------------------------------------------------------
bool Capsule3::ContainsPoint(const Vector3& point) const
{
	Vector3 spinePt;
	float distance = FindNearestPoint(point, start, end, spinePt);

	return distance < radius;
}


//-------------------------------------------------------------------------------------------------
Vector3 Capsule3::GetCenter() const
{
	return 0.5f * (end + start);
}


//-------------------------------------------------------------------------------------------------
void Capsule3::GetSupportPoint(const Vector3& direction, Vector3& out_point) const
{
	float startDot = DotProduct(start, direction);
	float endDot = DotProduct(end, direction);

	Vector3 endPoint = (startDot > endDot ? start : end);	
	out_point = endPoint + radius * direction.GetNormalized();
}

