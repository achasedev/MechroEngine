//-------------------------------------------------------------------------------------------------
int LineSegment3::GetSupportPoint(const Vector3& direction, Vector3& out_supportPt) const
{
	float aDot = DotProduct(direction, m_a);
	float bDot = DotProduct(direction, m_b);

	out_supportPt = (aDot > bDot ? m_a : m_b);
	return -1;
}


//-------------------------------------------------------------------------------------------------
Vector3 LineSegment3::GetCenter() const
{
	return 0.5f * (m_a + m_b);
}
