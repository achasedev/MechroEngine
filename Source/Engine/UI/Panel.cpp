//-------------------------------------------------------------------------------------------------
void Panel::Render() const
{
	Matrix44 modelMatrix = CalculateModelMatrix();
	m_renderable->SetRenderableMatrix(modelMatrix);

	g_renderContext->DrawRenderable(*m_renderable);

	UIElement::Render();
}
