void SetDxDeviceChildName(ID3D11DeviceChild* dxDeviceChild, const char* file, int lineNumber, const std::string& name)
{
	ASSERT_RETURN(dxDeviceChild != nullptr, NO_RETURN_VAL, "Device Child is null!");
	ASSERT_RETURN(name.size() > 0, NO_RETURN_VAL, "Null string!");
	
	std::string fullname = Stringf("\n%s(%i)\n%s", file, lineNumber, name.c_str());

	dxDeviceChild->SetPrivateData(WKPDID_D3DDebugObjectName, (UINT)fullname.size(), fullname.c_str());
}

