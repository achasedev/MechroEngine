//-------------------------------------------------------------------------------------------------
uint32 RefCount::AddRef()
{
	LockJanitor lockJanitor(m_lock);
	return ++m_count;
}


//-------------------------------------------------------------------------------------------------
uint32 RefCount::Release()
{
	LockJanitor lockJanitor(m_lock);
	return --m_count;
}
