uint32 HashData(void* data, size_t byteSize)
{
	// djb2 hash function, by Dan Bernstein
	uint32 hashValue = 5381;
	const uint8* p = (uint8*)data;

	for (size_t i = 0; i < byteSize; ++i)
	{
		uint32 currByte = (uint32) *p;
		hashValue = ((hashValue << 5) + hashValue) + currByte;

		p++;
	}

	return hashValue;
}


